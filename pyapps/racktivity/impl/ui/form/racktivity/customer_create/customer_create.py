def main(q, i, p, params, tags):
    cloudApi = i.config.cloudApiConnection.find('main')
    form = q.gui.form.createForm()
    
    tab = form.addTab("main", "Create Customer")
    tab.addText("custName", "Name", optional=False)
    tab.addMultiline("custDescription", "Description")
    tab.addText("custAddress", "Address", optional=False)
    tab.addText("custCity", "City", optional=False)
    tab.addChoice("custCountry", "Country", dict(enumerate(COUNTRIES)))
    
    valid = False
    while not valid:
        form.loadForm(q.gui.dialog.askForm(form))
        #validate data
        tab = form.tabs['main']
        username = tab.elements['custName'].value
        if cloudApi.customer.find(name = username)['result']['guidlist']:
            tab.elements['custName'].message = "Customer with the same name already exists"
            tab.elements['custName'].status = "error"
        else:
            valid = True
    tab = form.tabs['main']
    cloudApi.customer.create(name=tab.elements['custName'].value,
                            description=tab.elements['custDescription'].value,
                            address=tab.elements['custAddress'].value,
                            city=tab.elements['custCity'].value,
                            country=COUNTRIES[int(tab.elements['custCountry'].value)])
    
    q.gui.dialog.showMessageBox("Customer '%s' is being created" % tab.elements['custName'].value, "Create Customer")
    
