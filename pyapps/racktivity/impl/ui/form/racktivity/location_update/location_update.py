def getTagString(q, tab):
    labels = None
    labelsvalue = tab.elements['labels'].value
    trim = lambda s: s.strip()
    if labelsvalue:
        labels = set(map(trim, labelsvalue.split(',')))

    tags = dict()
    tagsvalue = tab.elements['tags'].value
    if tagsvalue:
        for tag in map(trim, tagsvalue.split(',')):
            tagslist = tag.split(':')
            tags[tagslist[0].strip()] = tagslist[1].strip()
    return q.base.tags.getTagString(labels, tags)

def main(q, i, p, params, tags):
    cloudapi = i.config.cloudApiConnection.find('main')
    location = cloudapi.location.getObject(params['extra']['locationguid'])
    locationform = q.gui.form.createForm()
    locationtab = locationform.addTab('location', 'Update Campus')
    locationtab.addText('name', 'Name', optional=False, value=location.name)
    locationtab.addMultiline('description', 'Description', value=location.description)
    locationtab.addText('alias', 'Alias', value=location.alias)
    locationtab.addText('address', 'Address', value=location.address)
    locationtab.addText('city', 'City', value=location.city)
    locationtab.addChoice("country", "Country", dict(enumerate(COUNTRIES)), selectedValue=COUNTRIES.index(location.country))
    locationtab.addText("latitude", "Latitude", validator="^\d*(\.\d+)?$", value=location.coordinates.latitude)
    locationtab.addText("longitude", "Longitude", validator="^\d*(\.\d+)?$", value=location.coordinates.longitude)
    locationtab.addYesNo('public', 'Is Public location', selectedValue=1 if location.public else 0)
    tagobj = q.base.tags.getObject(location.tags or '')
    locationtab.addText('tags', 'Tags', helpText='Enter tags in the form of tag1:value1,tag2:value2', 
                        value=",".join(["%s:%s" % (k, v) for k, v in tagobj.tags.iteritems()]))
    locationtab.addText('labels', 'Labels', helpText='Enter labels as comma separated values e.g. label1,label2', 
                        value=",".join(tagobj.labels) )
    locationform.loadForm(q.gui.dialog.askForm(locationform))
    locationtab = locationform.tabs['location']
    tagstring = getTagString(q, locationtab)
    
    lat = locationtab.elements['latitude'].value or 0.0
    long = locationtab.elements['longitude'].value or 0.0
    
    returnedvalue = cloudapi.location.updateModelProperties(locationguid=location.guid,
                                                            name=locationtab.elements['name'].value,
                                                            description=locationtab.elements['description'].value,
                                                            alias=locationtab.elements['alias'].value,
                                                            address=locationtab.elements['address'].value,
                                                            city=locationtab.elements['city'].value,
                                                            country=COUNTRIES[int(locationtab.elements['country'].value)],
                                                            coordinatesinfo={'latitude': float(lat),
                                                                             'longitude': float(long)},
                                                            tags=tagstring,
                                                            public=True if locationtab.elements['public'].value=='true' else False)

    q.gui.dialog.showMessageBox('Campus "%s" is being updated' % locationtab.elements['name'].value, "Update Campus")

